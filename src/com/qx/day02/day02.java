package com.qx.day02;

public class day02 {
    public static void main(String[] args) {
        //算数运算
        int a = 15;
        int b = 3;

        //a+b  15+3
        System.out.println(a + b);
        //a-b 15-3
        System.out.println(a - b);
        //a x b  15 x 3
        System.out.println(a * b);
        //a / b  15 /3
        System.out.println(a / b);
        // 取余数  0
        System.out.println(a % b);
        //自增
        System.out.println(a++);
        //自减
        System.out.println(a--);

        // TODO:      了解 a++  和 ++ a    a--   和--a


        int c = 1;
        System.out.println("c=" + c++);
        int d;
        d = c++;
        System.out.println("d=" + d);

    }
}
